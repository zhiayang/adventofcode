struct v3
{
	v3(int x, int y, int z) : x(x), y(y), z(z) { }

	int x;
	int y;
	int z;

	bool operator == (const v3& v) const { return x == v.x && y == v.y && z == v.z; }
	bool operator < (const v3& v) const { return x < v.x && y < v.y && z < v.z; }

	v3 operator + (const v3& v) { return v3(x + v.x, y + v.y, z + v.z); }
	v3 operator - (const v3& v) { return v3(x + v.x, y + v.y, z + v.z); }

	v3 operator += (const v3& v) { x += v.x; y += v.y; z += v.z; return *this; }
	v3 operator -= (const v3& v) { x -= v.x; y -= v.y; z -= v.z; return *this; }
};

namespace std
{
	template <class t>
	inline int hash_combine(int seed, t const& v)
	{
		return seed ^ hash<t>()(v) + 0x9e3779b9 + (seed<<6) + (seed>>2);
	}

	template<> struct hash<v3>
	{
		typedef v3 argument_type;
		typedef std::size_t result_type;

		result_type operator()(argument_type const& v) const noexcept
		{
			auto hx = std::hash<int>{}(v.x);
			auto hy = std::hash<int>{}(v.y);
			auto hz = std::hash<int>{}(v.z);

			return hash_combine(hash_combine(hash_combine(0, hx), hy), hz);
		}
	};
}

